              <h4>¿Cómo usamos la información recogida?</h4>
              <p>Los datos personales recogidos por teleSUR y sus proveedores de servicios, serán almacenados en un entorno seguro, donde serán tratados como “datos confidenciales” y sólo serán utilizados con el objetivo por los cuales fueron recogidos, de acuerdo con lo indicado en nuestro sitio web o según sea requerido para cumplir con nuestras obligaciones legales, por ejemplo, los datos suministrados para recibir boletines electrónicos de teleSUR serán utilizados para dar curso a la solicitud expresada y enviar información periódicamente.</p>

              <h4>• Traspaso de información personal</h4>
              <p>Los datos personales recogidos no serán en ningún término vendidos, arrendados y/o compartidos con terceros, a menos que nos sea exigido por ley revelar dicha información.</p>

              <h4>• Información recogida, alojamiento y procesamiento</h4>
              <p>Nuestro sitio web se encuentra alojado en un tercero, un proveedor de servicios, quien por lo tanto, podrá tener acceso y procesar la información de la persona recogida por nosotros, asimismo podemos recurrir a terceros para procesar su información personal, por ejemplo, ese tercero puede procesar sus datos personales para dar curso a las expresiones de interés con ocasión de unirse a teleSUR, tramitar donativos o procesar y enviar información a las personas que decidan suscribirse a nuestros boletines electrónicos. La utilización de su información personal será procesada únicamente en nombre de teleSUR y están obligados por estrictas condiciones de confidencialidad.</p>

