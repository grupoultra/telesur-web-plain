              <h4>¿Qué información nos permite recoger?</h4>
              <p>Puede navegar a través de la mayoría de las páginas sin revelarnos su identidad o ningún dato personal.</p>
              <p>Nuestros servidores almacenan archivos de registros anónimos durante las visitas que realizan los usuarios al sitio web de teleSUR. La información recogida es utilizada para elaborar estadísticas, por ejemplo, sobre la hora de acceso al sitio, las páginas web que han remitido al usuario a nuestro sitio, el tipo de navegador que usan nuestros visitantes y las páginas web que visitan.</p>
